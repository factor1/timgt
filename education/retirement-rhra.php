                  <h1 class="education-article__heading">Retirement Health Reimbursement Arrangement (RHRA)</h1>

                  <p>Medical expenses keep inflating and most people don’t realize that there are additional medical expenses above what Medicare covers that you will need to be responsible for in retirement. Fidelity Investments did a recent study showing these costs to be on average around 240k for a 65 year old couple. And this doesn’t include Long Term Care expenses if you need that type of care! We should have another white paper/video out shortly about Long Term Care. Clients in their late 40’s or early 50’s with a current life insurance policy and have additional cash to save should reach out to Todd Foster ASAP to discuss an innovative way to secure LTC).</p>

<p>Here is some basic information on the program…</p>

<h3>Retirement Health Reimbursement Arrangement</h3>

<p>A Retirement Health Reimbursement Arrangement (RHRA) allows employers to provide their employees with tax-free money to help them pay for qualified medical expenses incurred during retirement. The Retirement HRA is allowed under Section 213(d) of the Internal Revenue Code, which defines what expenses are considered qualified and can be paid out of the Retirement HRA.</p>

<h3>Extra Money for Health Care Expenses during Retirement</h3>

<p>United now has a Retiree Health Reimbursement Arrangement (RHRA) for pilots. This account offers financial support to help pay for health care expenses you may have during your retirement.</p>

<h3>An Employer-funded Account for Retirees</h3>

<p>You don’t contribute any money for this account. United provides the funding for your Retirement HRA. These funds are made up of excess PRAP contributions over the maximum total 401k contributions allowed. This amount is $52,000 in 2014, and will change over time. If the company contribution plus your personal contribution exceeds the $52,000 maximum, a portion of the company contribution that equals this excess is designated as an RHRA contribution.</p>

<p>Your Retirement HRA will grow through employer contributions and investment earnings. Your Retirement HRA account will become available to reimburse qualified medical expenses once you attain normal retirement age and have ceased industry employment. This money is NOT held in the PRAP. If you have a balance, it is in another account that you can see through the benefits web-site (ybr.com/united). It is invested in one of the Vanguard Target Date funds that match your retirement date.</p>

<p>Typical qualified retiree medical expenses include COBRA premiums, copays, deductibles and premiums for health care coverage. Retirees over age 65 can use their Retirement HRA to cover premiums for Medicare Parts A, B and D, a Medicare Advantage Plan or Supplemental Plan. Refer to your Summary Plan Description (SPD) or your ALPA reps for information about other eligible expenses, or for a complete list of eligible and ineligible expenses, refer to IRS Publication 502. You can order the publication by calling 1-800-829-3676 or view it online at <a href="http://www.irs.gov/pub/irs-pdf/p502.pdf">www.irs.gov/pub/irs-pdf/p502.pdf</a>.</p>

<p>As long as there is money in your account, you can use the funds to pay for eligible expenses.</p>

<h3>More Money without Any More Taxes</h3>

<p>You don’t pay taxes on the money in your Retiree HRA or your qualified reimbursed expenses.</p>

<p>This is a great benefit folks. You should definitely consider the benefits here when deciding on contributions vs. other dollar uses.</p>

<p>Important: To find out all specifics of your RHRA plan, review the Summary Plan Description (SPD), or your ALPA reps.</p>

<h3>Find a Medicare Plan that Suits Your Needs&lt;</h3>

<p>When you reach age 65, you should coordinate, as much as possible, any supplemental Medicare plan with other benefits you may have. You may find using the services provided by Joppel, a Medicare decision support provider, a help in taking the confusion out of making the right Medicare plan. Use the Joppel website to identify Medicare coverage choices that match your individual needs. It’s easy to compare plans online: <a href="http://www.joppel.com">www.joppel.com</a>. (There are many other on-line services. Joppel is very user-friendly.)</p>

<p>In addition, if you are a USAA member, you can obtain a great deal of information from them.</p>

