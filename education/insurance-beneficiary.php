                  <h1 class="education-article__heading">Would You Like To Be The Beneficiary Of Your Own Life Insurance Policy While You Are Living?</h1>

                  <p>One of the disadvantages of traditional life insurance is the lack of living benefits available for the insured or policy owner. You pay premiums almost your entire adult life primarily to secure your family after your death, pay off debts like a mortgage or to leave a legacy. Unfortunately, there is no real benefit to the actual policy owner or insured because they are no longer living.</p>

<p>More recently, more policies are allowing you to draw against the face value of the policy in the event of a critical, chronic or terminal health event. A heart attack, severe stroke or longer term illnesses like Parkinson’s and Alzheimer’s can destroy ones financial plan. Therefore, these improvements can greatly improve ones’ odds of a successful retirement. You should check to see if your current policy has this protection.</p>

<p><strong><em>What happens if you don&rsquo;t need the death benefit for medical costs? Is there a way you can still benefit while you are living?</em></strong></p>

<p>One of the major insurance companies has recently announced a solution that can tackle 3 important financial concerns as you age that can greatly increase the benefits to owning life insurance. Please note that the 3 financial concerns are GUARANTEED and cannot be changed by the insurance company.</p>

<h3>These new type of policies will pay a benefit if:</h3>

<h4>1. You die too soon</h4>

<ol>
<li>Pays a TAX-FREE lump sum death benefit</li>
<li>Did you know that people aged 40-50 have about a 25% chance of not making it to age 65?</li>
</ol>

<h4>2. You need chronic health care</h4>

<ol>
<li>Pays a TAX-FREE check monthly if you need home health, assisted living or a nursing home due to a chronic health condition.</li>
<li>EVERY 68 seconds someone develops Alzheimer’s disease</li></li>
<li>EVERY 34 seconds an American suffers a coronary event</li></li>
<li>EVERY 40 seconds an American suffer a stroke</li></li>
<li>70% of persons over age 65 will not be able to perform 2 out of 6 Activities of Daily Living (ADLs) and thus trigger a claim with this policy.</li></li>
</ol>

<h4>3. You live too long</h4>

<ol>
<li>The insurance company will pay you the full death benefit while you are alive if you live to age 85 (paid out over a 10 year period. If insured dies prior to the full living payout, the remainder will be left to the insured’s chosen beneficiary).</li></li>
</ol>

<p>If you have existing life policies that do not have these new enhancements or are looking to start an insurance solution to complete your financial plan, give us a call to see how this type of insurance can make buying and paying for insurance a more palatable expense as you plan and enjoy your retirement.</p>

