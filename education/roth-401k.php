                  <h1 class="education-article__heading">Roth 401(k) Contributions vs. Traditional 401(k) Contributions</h1>

                  <p>We have been getting a lot of questions lately asking if it is better to make Roth contributions or Traditional contributions. Since this is more of a tax question than an investment question we always recommend talking this over with your tax preparer. Uncle Sam is going to force us to pay taxes either way, the Roth vs. Traditional debate affects when those are paid. For clients who are in their peak earning years waiting to pay those taxes in retirement when you are in a lower tax bracket may make more sense. Or if you are just starting your career paying the taxes now and pulling out tax free in retirement may seem like a better option. What makes this decision tough is there is a huge unknown on what the tax code will be 10-30 years from now when you start pulling money out of your retirement account. For most clients who are in the middle of their career that unknown tax code makes the decision a hard one. Fidelity put out a great article explaining the differences between the two options and we have been using it more and more as a reference. Please click on the link below to learn more about the differences.</p>

