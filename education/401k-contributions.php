                  <h1 class="education-article__heading">Benefits of Contributing to your 401(k)</h1>

                  <h3>Should I max out my personal 401k contributions?</h3>

<p>We are asked this question often. Here is our question: How many times would you give me seven bucks in return for ten? That’s about the average benefit you get in tax breaks – a senior left-seat driver will get more. The 401k contributions are before-tax dollars, so your W2 amount on which your tax is based, leaves your contribution out. So, in effect, for a $7 effective outlay you are getting the benefit of a $10 contribution.</p>

<p>NOTE: I’ve talked to a few folks who are under the impression that since there is a phase out of IRA contributions with increasing AGI amounts, the same is true for 401k contributions. Not so - there is no income limit on 401k contributions. So, even if you don’t get the IRA deduction, you do qualify for the 401k benefit.</p>

<p><u>So now you know that contributing to your 401k actually lowers your taxable income each year so you pay less in taxes</u>. Let’s look at what personal contributions do to portfolio values and income potential next.</p>

<p>Now let’s take a 40 year old FO, who begins at that age to contribute the max. By age 60, under typical investment conditions in a balanced portfolio, he or she would accumulate about <u>$740,000 additional in his or her 401k, over the amount he or she would have accumulated with just company contributions</u>. That amount may be enough to give him or her the option of early retirement. That amount could comfortably produce another 30k of income in retirement.</p>

<p>By age 65, he or she would accumulate about $1.2 million additional. This would be an additional 54k of income in retirement.</p>

<p>Is that worth it? We think so.</p>

<p>NOTE: The above calculations take into account current personal and employer contribution limits. These limits change over time.</p>

<p>Here is a graphical display of the projected 401(k) balances and possible income achieved from those portfolio values. We used an United Pilot as an example but these numbers will be very similar across all carriers.</p>

<p><img src="/img/education/graph-401k.png" alt=""></p>

<p>The above chart compares the hypothetical values of a PRAP portfolio and the spending that would be available from a pilot’s PRAP account accumulation:</p>

<ul>
<li>UAL ONLY - a typical pilot’s portfolio and available spending at age 60, with only UAL contributions to his PRAP.</li>
<li>UAL + Pilot at age 60 – a typical pilot’s portfolio and available spending with company contributions PLUS his or her maximum PRAP contributions.</li>
<li>UAL + Pilot at age 65 – a typical pilot’s portfolio and available spending with company contributions PLUS his or her maximum PRAP contributions, PLUS the benefit of additional compounding of the PRAP account gained by waiting until age 65 to retire.</li>
</ul>

<p>NOTE: Social Security and PBGC benefits are disregarded in spending amounts.</p>

<p>In summary, personal contributions to your 401k should be right up there near the top of your financial priority list. Is there anything that should be above it in priority? Absolutely – credit card and high interest consumer debt, (i.e. any double digit interest debt), should be the first thing you take out. But as soon as that’s done, get on line and adjust your 401k contributions as high as possible.</p>

<p>Call us at the office if you have any questions.</p>

