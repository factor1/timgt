                <h3 style="text-align:left;margin-top:30px">Retirement Health Reimbursement Arrangement</h3>
                <p>A Retirement Health Reimbursement Arrangement (RHRA) allows employers to provide their employees with tax-free money to help them pay for qualified medical expenses incurred during retirement. The Retirement HRA is allowed under Section 213(d) of the Internal Revenue Code, which defines what expenses are considered qualified and can be paid out of the Retirement HRA.</p>

                <h3 style="text-align:left;margin-top:30px">Extra Money for Health Care Expenses during Retirement</h3>
                <p>United now has a Retiree Health Reimbursement Arrangement (RHRA) for pilots. This account offers financial support to help pay for health care expenses you may have during your retirement.</p>

                <h3 style="text-align:left;margin-top:30px">An Employer-funded Account for Retirees</h3>
                <p>You don’t contribute any money for this account. United provides the funding for your Retirement HRA. These funds are made up of excess PRAP contributions over the maximum total 401k contributions allowed. This amount is $52,000 in 2014, and will change over time. If the company contribution plus your personal contribution exceeds the $52,000 maximum, a portion of the company contribution that equals this excess is designated as an RHRA contribution.</p>
                <p>Your Retirement HRA will grow through employer contributions and investment earnings. Your Retirement HRA account will become available to reimburse qualified medical expenses once you attain normal retirement age and have ceased industry employment. This money is NOT held in the PRAP. If you have a balance, it is in another account that you can see through the benefits web-site (ybr.com/united). It is invested in one of the Vanguard Target Date funds that match your retirement date.</p>
                <p>Typical qualified retiree medical expenses include COBRA premiums, copays, deductibles and premiums for health care coverage. Retirees over age 65 can use their Retirement HRA to cover premiums for Medicare Parts A, B and D, a Medicare Advantage Plan or Supplemental Plan. Refer to your Summary Plan Description (SPD) or your ALPA reps for information about other eligible expenses, or for a complete list of eligible and ineligible expenses, refer to IRS Publication 502. You can order the publication by calling 1-800-829-3676 or view it online at www.irs.gov/pub/irs-pdf/p502.pdf.</p>
                <p>As long as there is money in your account, you can use the funds to pay for eligible expenses.</p>

                <h3 style="text-align:left;margin-top:30px">More Money without Any More Taxes</h3>
                <p>You don’t pay taxes on the money in your Retiree HRA or your qualified reimbursed expenses.</p>
                <p>This is a great benefit folks. You should definitely consider the benefits here when deciding on PRAP contributions vs. other dollar uses.</p>
                <p>Important: To find out all specifics of your RHRA plan, review the Summary Plan Description (SPD), or your ALPA reps.</p>

